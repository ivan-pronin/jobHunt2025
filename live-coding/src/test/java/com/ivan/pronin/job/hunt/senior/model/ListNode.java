package com.ivan.pronin.job.hunt.senior.model;

/**
 * @author Ivan Pronin
 * @since 10.07.2025
 */
public class ListNode {

    public int val;

    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
